# In computer programming, a comment is a programmer-readable
# explanation or annotation in the source code of a computer program.
# They are added with the purpose of making the source code easier
# for humans to understand, and are generally ignored by compilers and interpreters.
# The syntax of comments in various programming languages varies considerably.
