# Assigning string to a variable
a = "34"
print("a is ", type(a))

b = 34
print("b is ", type(b))

c = 34.0
print("c is ", type(c))

d = "68"
print("a+d is ", a+d, type(a+d))
print("b+c is ", b+c, type(b+c))
